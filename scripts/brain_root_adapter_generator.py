"""
brain_root_adapter_generator.py
================================
Generates BrainMechanism adapters for the phenomenological/integrative
mechanism files sitting loose at brain/ root (NOT in the 5 layer subdirs).

Skips:
  - files identical to a legacy/ counterpart (already covered by
    scripts/legacy_adapter_generator.py)
  - files that don't define any class with a process() method
  - infrastructure files (base_mechanism, bootstrap, core_loop, etc.)

Adapter imports `from brain.{module} import {class}` (NOT legacy).
"""
