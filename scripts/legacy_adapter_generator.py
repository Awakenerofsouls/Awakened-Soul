#!/usr/bin/env python3
"""
legacy_adapter_generator.py
=============================
One-shot converter that takes legacy/ files (using the old
`process(pirp_context)` pattern) and generates BrainMechanism adapter
modules in brain/{layer}/ so the brain_runner can load and tick them.

DESIGN
------
Each generated adapter is a thin BrainMechanism subclass that:
  1. Constructs the legacy class (with auto-supplied db_path if needed)
  2. In tick(input_data), passes input_data["prior_results"] as
     pirp_context to legacy_obj.process(pirp_context)
  3. Returns the legacy class output state as a dict

ANATOMICAL LAYER ASSIGNMENT
---------------------------
Heuristic on file path + class name:
  legacy/becoming/        -> limbic       (plasticity, fracture, growth)
  legacy/inner_voice/     -> neocortical  (speech, narrative)
  legacy/knowing/         -> integration  (metacognition, gaps)
  legacy/felt_presence/   -> limbic       (longing, mood, embodiment)
  legacy/substrate/       -> foundational (rhythm, identity boundary)
  legacy/third_eye/       -> integration  (compressor, meta-stability)
  legacy/self/            -> integration  (meta-consciousness, drift)
  legacy/narrative/       -> neocortical  (narrative weaving)
  legacy/value/           -> neocortical  (ethical evaluation)
  legacy/life/            -> integration  (planning, scheduling)
  legacy/social/          -> limbic       (relational engine)
  legacy/texture/         -> limbic       (residue, sediment, asymmetry)
  legacy/offline/         -> integration  (dream-mode)
  legacy/ root            -> integration  (default for phenomenology)

USAGE
-----
  python3 scripts/legacy_adapter_generator.py
    --src ~/awakened-soul-repo/legacy
    --repo ~/awakened-soul-repo
    [--dry-run]
"""
from __future__ import annotations

import argparse
import ast
import sys
from pathlib import Path
from typing import Optional

