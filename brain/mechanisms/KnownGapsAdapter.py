    def _detect_from_signals(self, signals: list, tick: int):
        question_markers = [
            "why", "what if", "how come", "i don't know", "unsure",
            "unclear", "uncertain", "don't understand", "confused",
            "wonder", "wondering", "still don't", "never resolved",
        ]
        suspicion_markers = [
            "i think maybe", "possibly", "might be", "could be that",
            "not sure but", "suspect", "feeling like", "sense that",
        ]
        avoidance_markers = [
            "not going to", "avoiding", "don't want to think",
            "leaving that", "skipping", "moving on from",
        ]

        for sig in signals:
            text = sig.get("text", "").lower()
            if not text:
                continue

            if any(m in text for m in question_markers):
                label = self._extract_label(text, "question")
                if label:
                    self.register(label, text[:300], KNOWN_UNKNOWN, tick, "signal_detection")

            elif any(m in text for m in suspicion_markers):
                label = self._extract_label(text, "suspicion")
                if label:
                    self.register(label, text[:300], SUSPICION, tick, "signal_detection")

            elif any(m in text for m in avoidance_markers):
                label = self._extract_label(text, "avoidance")
                if label:
                    self.register(label, text[:300], AVOIDED, tick, "signal_detection")

    def _extract_label(self, text: str, gap_type: str) -> Optional[str]:
        stopwords = {
            "why", "what", "that", "this", "the", "and", "but", "for",
            "with", "from", "have", "been", "about", "think", "know",
            "going", "want", "feel", "like", "just", "still", "maybe",
            "could", "would", "should", "dont", "doesnt", "isnt", "not",
        }
        words = re.findall(r"\b\w{4,}\b", text.lower())
        content_words = [w for w in words if w not in stopwords][:5]

        if len(content_words) < 2:
            return None

        return f"{gap_type}:{' '.join(content_words[:4])}"

