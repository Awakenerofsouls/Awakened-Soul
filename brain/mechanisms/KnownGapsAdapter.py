    def _apply_decay(self, tick: int):
        """Reduce weight of gaps that weren't seen this tick. Auto-close old zeros."""
        try:
            with sqlite3.connect(self.db_path) as conn:
                conn.execute("""
                    UPDATE known_gaps
                    SET weight = MAX(0.0, weight - ?)
                    WHERE status = 'active'
                    AND last_seen_tick != ?
                """, (DECAY_RATE, tick))

                conn.execute("""
                    UPDATE known_gaps
                    SET status = 'auto_closed'
                    WHERE status = 'active'
                    AND weight <= 0.0
                    AND (? - last_seen_tick) > ?
                """, (tick, AUTO_CLOSE_TICKS))

                conn.commit()
        except Exception as e:
            logger.debug("KnownGaps: decay update failed — %s", e)

