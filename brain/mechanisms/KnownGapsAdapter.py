    def register(
        self,
        label: str,
        description: str,
        tier: str,
        tick: int = 0,
        source: str = "manual",
        initial_weight: float = 0.5,
    ) -> int:
        """
        Register a new gap or reinforce an existing one by label.
        Returns the gap id.
        """
        if tier not in VALID_TIERS:
            tier = KNOWN_UNKNOWN

