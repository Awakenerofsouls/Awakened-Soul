    # Decay
    # ------------------------------------------------------------------

    def _apply_decay(self, tick: int):
        try:
            with sqlite3.connect(self.db_path) as conn:
                conn.execute("""
                    UPDATE desires
                    SET intensity = MAX(0.0, intensity - persistence_rate)
                    WHERE status = 'active'
                    AND last_tick != ?
                """, (tick,))

                conn.execute("""
                    UPDATE desires
                    SET status = 'dormant'
                    WHERE status = 'active'
                    AND intensity < ?
                """, (DORMANT_THRESHOLD,))

                conn.commit()
        except Exception as e:
            logger.debug("DesireArchitecture: decay failed — %s", e)

